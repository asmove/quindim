        vex_x = zeros(degree, 1);
        
        if(degree == 2)
            x = 1;
        end
        
        if(degree == 3)
            alpha = 0.5;
        
            % Guard
            if((alpha == 0)||(alpha == 2/3)||(alpha == 1))
                msg = 'Alpha cannot be 0, 2/3 or 1.';
                msg = [msg, 'See https://arxiv.org/abs/1809.06114'];
                error(msg);
            end
        end
        
        switch degree
            case 1
                vec_t = 0;
                vec_k = 0;
