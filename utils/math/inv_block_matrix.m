function [inv_A_C1, ...
          inv_A_C2, ...
          inv_A_C3] = inv_block_matrix(A_11, A_12, A_21, A_22)
    C_1 = A_11 - A_12*inv(A_22)*A_21;
    C_2 = A_22 - A_21*inv(A_11)*A_12;
    
    inv_A_C1 = [inv(C_1), -inv(A_11)*A_12*inv(C_2); ...
             -inv(C_2)*A_21*inv(A_11), inv(C_2)];
         
    inv_A_C2 = [inv(C_1), -inv(C_1)*A_12*inv(A_22); ...
                -inv(A_22)*A_21*inv(C_1), inv(C_2)];
            
    inv_A_C3 = [inv(C_1), -inv(C_1)*A_12*inv(A_22); ...
                -inv(C_2)*A_21*inv(A_11), inv(C_2)];
end